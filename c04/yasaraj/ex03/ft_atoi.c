int	ft_atoi(char *str)
{
	int neg;
	int res;

	neg = 1;
	res = 0;
	if (str)
		while (*str == '\t' || *str == '\n' || *str == '\v' ||
				*str == '\f' || *str == '\r' || *str == ' ')
			str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (res * neg);
}
